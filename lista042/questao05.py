'''
5. Fazer um algoritmo que pergunte a sigla de um estado brasileiro (UF -> Unidade Federativa), e ao final,
informe na tela se o estado inserido está ou não na região Sudeste.
'''

var = int(input("Informe um estado brasileiro com sua sigla: "))

if ( var == "RJ" ):
    print(f"{var} é da Região Sudeste")
elif ( var == "SP"):
    print(f"{var} é da Região Sudeste")
elif ( var == "MG" ):
    print(f"{var} é da Região Sudeste")
elif ( var == "ES"):
    print(f"{var} é da Região Sudeste")
else:
    print("Esse estado não é da Região Sudeste")




